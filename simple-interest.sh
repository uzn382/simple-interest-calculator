#!/bin/bash

echo "=============================="
echo "   Simple Interest Calculator "
echo "=============================="

# Мәлімет енгізу
read -p "Негізгі сома (Principal): " P
read -p "Пайыздық мөлшерлеме (%): " R
read -p "Уақыт (жыл): " T

# Тексеру (бос емес пе)
if [[ -z "$P" || -z "$R" || -z "$T" ]]; then
    echo "Қате: барлық өрістерді толтырыңыз!"
    exit 1
fi

# Формула: SI = (P * R * T) / 100
SI=$(echo "scale=2; ($P * $R * $T) / 100" | bc)

echo "------------------------------"
echo "Нәтиже:"
echo "Қарапайым пайыз = $SI"
echo "------------------------------"
